def accuracy(y, y_hat):
    """
    """

    tp = sum(y[y_hat == 1] == 1)
    fn = sum(y[y_hat == 0] == 1)
    fp = sum(y[y_hat == 1] == 0)
    tn = sum(y[y_hat == 0] == 0)

    return (tp + tn)/(fp + fn + tp + tn)

